package Day4_Initialisation;

public class o3_static_declaration {
	
	static int a;
	static int b;
	static int c;
	
	public static void main(String[] args) {

		o3_static_declaration.a=1000;
		o3_static_declaration.b=2000;
		o3_static_declaration.c=3000;
		
		System.out.println(o3_static_declaration.a);
		System.out.println(o3_static_declaration.b);
		System.out.println(o3_static_declaration.c);
		
		
		
	}

}






