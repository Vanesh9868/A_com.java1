package Dayz10_looping_statement;

public class A_forloop {

	public static void main(String[] args) {
		
		
		for(int i=1; i<=5;i++)
		{
			System.out.print(i);
			System.out.println(" - yogesh");
		}
		
		System.out.println("block end");
		
		for (int i=5; i>=1; i--)
		{
			System.out.print(i);
			System.out.println(" - sunil");
			
		}
		
	
	}

	
}
