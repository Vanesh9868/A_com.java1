package a3_Star_program;

public class a3_star_program {

	public static void main(String[] args) {

		for(int i=1; i<=4; i++)
		{
			for (int b=1; b<=i;b++)
			{
				System.out.print("*");
			}
			
			System.out.println();
		}
			
		for (int d=3; d>=1; d--)
		{
			for (int k=1; k<=d; k++)
			{
				System.out.print("*");
			}
				
				System.out.println();
		}	
			
			
			
			
			
			
	}

}
