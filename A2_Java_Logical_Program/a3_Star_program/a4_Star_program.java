package a3_Star_program;

public class a4_Star_program {

	public static void main(String[] args) {

		
		for(int i=1;i<=4;i++)
		{
			for(int a=3;a>=i;a--)
			{
				System.out.print(" ");
			}
			
			for(int b=1;b<=i;b++)
			{
				System.out.print("*");
			}
			
			System.out.println();
			
		}
		
		
		
		
	}

}
