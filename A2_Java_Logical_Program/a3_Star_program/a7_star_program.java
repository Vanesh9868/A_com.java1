package a3_Star_program;

public class a7_star_program {

	public static void main(String[] args) {


		for (int i=1; i<=3; i++)
		{
			if (i==1)
			{
				System.out.println("*       * * * * *");
			}
			
			System.out.println("*       *        ");
			
		}
		                  
		System.out.println("* * * * * * * * *");
		
		for(int a=1; a<=4; a++)
		{
			
			System.out.println("        *       *");
			
			if (a==3)
			{
				System.out.println("* * * * *       *");
				break;
			}
			
		}
		

		
	}

}
