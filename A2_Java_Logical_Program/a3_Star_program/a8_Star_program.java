public class a8_Star_program {

	public static void main(String[] args) {


		for (int i=1; i<=2; i++)
		{
			if (i==1)
			{
				System.out.println("* * * *");
			}
			
			System.out.println("*     *");
			
			if (i==2)
			{
				
				System.out.println("* * * *");
			}
			
		}
		
	
}

}
