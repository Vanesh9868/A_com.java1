package a3_Star_program;

public class a9_pyramid {

	public static void main(String[] args) {

		for (int i=1; i<=5; i++)
		{
			for(int a=4;a>=i;a--)
			{			
				System.out.print(" ");
			}
			
			for (int b=1; b<=i; b++)
			{
					System.out.print("*");
			}
			for (int c=2; c<=i; c++)
			{
				System.out.print("*");
			}
			
		      System.out.println();
			
		      
		}
		
		
		
		
		
		
		
		
	}

}
