package a4_Operator_Swapping_logical_program;

public class a2_Swapping_number_without_using_thirdVariable {

	public static void main(String[] args) {


		int a=10;
		int b=20;
		
		a=a+b; //30
		b=a-b; //10
		a=a-b; //20
		
		System.out.println("a = "+a);
		System.out.println("b = "+b);
	
	   		
		
		
}

}
