package a2_Even_Odd;

public class a1_even_number {
	
/*
 * the number which is divisible by 2 is called the even number
 */

	public static void main(String[] args) {

		int a=9;
		
		if(a%2==0)
		{
			System.out.println("even number");
		}
		else 
		{
			System.out.println("not a even number");
		}

	
	}

}
