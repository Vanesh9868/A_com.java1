package a2_Even_Odd;

public class a2_even_or_odd_number {
	
/* 
 * 
 * the number which is divisible by 2 is called the even number
 *  
 */

	public static void main(String[] args) {

		int a=50; 

		if (a%2 ==0)
		{
			System.out.println("even");
		}
		else
		{
			System.out.println("odd");
		}

		
	}
	
}




