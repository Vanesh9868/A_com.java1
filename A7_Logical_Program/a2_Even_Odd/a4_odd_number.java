package a2_Even_Odd;

public class a4_odd_number {

/*
 * the number which is not divisible by 2 is called the odd number
 * 
 */
	public static void main(String[] args) {

		
		int a = 16;
		
		if (a%2!=0)
		{
			System.out.println("odd number");
		}
		else
		{
			System.out.println("not a odd number");
		}
		
		
	}
}
